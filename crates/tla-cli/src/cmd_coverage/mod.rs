/// Build the coverage analysis from parsed JSON output and optional spec.
fn build_coverage_analysis(
    output: &JsonOutput,
    spec_path: Option<&Path>,
    config_path: Option<&Path>,
) -> Result<CoverageAnalysis> {
    let total_states = output.statistics.states_found;
    let total_transitions = output.statistics.transitions;

    // Build per-action coverage from actions_detected.
    let mut actions: Vec<ActionCoverage> = if !output.actions_detected.is_empty() {
        build_from_actions_detected(&output.actions_detected, total_transitions)
    } else if let Some(ce) = &output.counterexample {
        // Fall back: infer from counterexample trace action names.
        build_from_trace_actions(&ce.states)
    } else {
        Vec::new()
    };

    // If we have a counterexample trace, augment with first/last seen levels.
    if let Some(ce) = &output.counterexample {
        augment_with_trace_levels(&mut actions, &ce.states);
    }

    // Sort actions by occurrences descending (hottest first).
    actions.sort_by(|a, b| b.occurrences.cmp(&a.occurrences));

    // Cross-reference with spec disjuncts if --spec is provided.
    let (spec_disjuncts, uncovered_actions) = if let Some(spec) = spec_path {
        cross_reference_spec(spec, config_path, &actions)?
    } else {
        (None, Vec::new())
    };

    let actions_exercised = actions.len();

    let hottest_action = actions.first().map(|a| a.name.clone());
    let coldest_action = actions
        .iter()
        .filter(|a| a.occurrences > 0)
        .min_by_key(|a| a.occurrences)
        .map(|a| a.name.clone());

    let coverage_ratio = spec_disjuncts.map(|total| {
        if total == 0 {
            1.0
        } else {
            actions_exercised as f64 / total as f64
        }
    });

    let summary = CoverageSummary {
        actions_exercised,
        actions_defined: spec_disjuncts,
        coverage_ratio,
        hottest_action,
        coldest_action,
        time_seconds: output.statistics.time_seconds,
        max_depth: output.statistics.max_depth,
    };

    Ok(CoverageAnalysis {
        spec_file: output.input.spec_file.clone(),
        module: output.input.module.clone(),
        status: output.result.status.clone(),
        total_states,
        total_transitions,
        actions,
        uncovered_actions,
        summary,
    })
}

/// Build action coverage from the `actions_detected` field.
fn build_from_actions_detected(
    actions: &[ActionInfo],
    total_transitions: usize,
) -> Vec<ActionCoverage> {
    actions
        .iter()
        .map(|a| {
            let percentage = if let Some(pct) = a.percentage {
                pct
            } else if total_transitions > 0 {
                (a.occurrences as f64 / total_transitions as f64) * 100.0
            } else {
                0.0
            };
            ActionCoverage {
                name: a.name.clone(),
                id: a.id.clone(),
                occurrences: a.occurrences,
                percentage,
                first_seen_level: None,
                last_seen_level: None,
            }
        })
        .collect()
}

/// Infer action coverage from counterexample trace state actions.
fn build_from_trace_actions(
    states: &[tla_check::StateInfo],
) -> Vec<ActionCoverage> {
    let mut action_counts: HashMap<String, usize> = HashMap::new();

    for state in states {
        if state.action.action_type != "initial" {
            *action_counts
                .entry(state.action.name.clone())
                .or_insert(0) += 1;
        }
    }

    let total: usize = action_counts.values().sum();
    action_counts
        .into_iter()
        .map(|(name, count)| {
            let percentage = if total > 0 {
                (count as f64 / total as f64) * 100.0
            } else {
                0.0
            };
            ActionCoverage {
                name,
                id: String::new(),
                occurrences: count,
                percentage,
                first_seen_level: None,
                last_seen_level: None,
            }
        })
        .collect()
}

/// Augment action coverage with first/last seen BFS levels from the trace.
fn augment_with_trace_levels(
    actions: &mut [ActionCoverage],
    states: &[tla_check::StateInfo],
) {
    // Build a map of action_name -> (first_index, last_index) from trace states.
    let mut level_map: HashMap<&str, (usize, usize)> = HashMap::new();
    for state in states {
        if state.action.action_type == "initial" {
            continue;
        }
        let name = state.action.name.as_str();
        let level = state.index;
        level_map
            .entry(name)
            .and_modify(|(first, last)| {
                if level < *first {
                    *first = level;
                }
                if level > *last {
                    *last = level;
                }
            })
            .or_insert((level, level));
    }

    for action in actions.iter_mut() {
        if let Some(&(first, last)) = level_map.get(action.name.as_str()) {
            action.first_seen_level = Some(first);
            action.last_seen_level = Some(last);
        }
    }
}

/// Cross-reference detected actions with spec-defined Next disjuncts.
///
/// Returns `(total_defined, uncovered_names)`.
fn cross_reference_spec(
    spec_path: &Path,
    config_path: Option<&Path>,
    detected: &[ActionCoverage],
) -> Result<(Option<usize>, Vec<String>)> {
    let source = std::fs::read_to_string(spec_path)
        .with_context(|| format!("Failed to read spec file: {}", spec_path.display()))?;

    let tree = tla_core::parse_to_syntax_tree(&source);
    let lower_result = tla_core::lower(tla_core::FileId(0), &tree);
    if !lower_result.errors.is_empty() {
        // Cannot analyze spec structure; skip cross-referencing.
        return Ok((None, Vec::new()));
    }

    let module = match &lower_result.module {
        Some(m) => m,
        None => return Ok((None, Vec::new())),
    };

    // Determine the Next operator name from config or default.
    let next_name = resolve_next_name(config_path)?;

    // Extract disjuncts from the Next operator.
    let disjuncts = extract_disjuncts_from_module(module, &next_name);
    if disjuncts.is_empty() {
        return Ok((None, Vec::new()));
    }

    let total_defined = disjuncts.len();
    let detected_names: std::collections::HashSet<&str> =
        detected.iter().map(|a| a.name.as_str()).collect();

    let uncovered: Vec<String> = disjuncts
        .into_iter()
        .filter(|name| !detected_names.contains(name.as_str()))
        .collect();

    Ok((Some(total_defined), uncovered))
}

/// Resolve the Next operator name from a config file, defaulting to "Next".
fn resolve_next_name(config_path: Option<&Path>) -> Result<String> {
    if let Some(cfg_path) = config_path {
        if cfg_path.exists() {
            let cfg_source = std::fs::read_to_string(cfg_path)
                .with_context(|| format!("read config file: {}", cfg_path.display()))?;
            let config = tla_check::Config::parse(&cfg_source).map_err(|errors| {
                anyhow::anyhow!("config parse failed with {} error(s)", errors.len())
            })?;
            if let Some(next) = config.next {
                return Ok(next);
            }
        }
    }
    Ok("Next".to_string())
}

/// Extract disjunct names from a module's Next operator definition.
///
/// For `Next == A \/ B \/ C`, returns `["A", "B", "C"]` if each disjunct is
/// a simple operator application. For complex expressions, returns the
/// pretty-printed expression text.
fn extract_disjuncts_from_module(
    module: &tla_core::ast::Module,
    operator_name: &str,
) -> Vec<String> {
    for unit in &module.units {
        if let tla_core::ast::Unit::Operator(def) = &unit.node {
            if def.name.node == operator_name {
                return extract_disjuncts(&def.body.node);
            }
        }
    }
    Vec::new()
}

/// Recursively extract disjuncts from `A \/ B \/ C`.
fn extract_disjuncts(expr: &tla_core::ast::Expr) -> Vec<String> {
    match expr {
        tla_core::ast::Expr::Or(lhs, rhs) => {
            let mut result = extract_disjuncts(&lhs.node);
            result.extend(extract_disjuncts(&rhs.node));
            result
        }
        // If disjunct is a simple operator reference, use its name.
        tla_core::ast::Expr::Ident(name, _) => vec![name.clone()],
        // If disjunct is an operator application with no args (e.g., `Foo`),
        // try to extract a readable name.
        other => vec![tla_core::pretty_expr(other)],
    }
}


use super::{DetectedAction, DetectedActionId};
use rustc_hash::FxHashMap;

/// Coverage statistics for the entire model checking run
#[derive(Debug, Clone, Default)]
pub struct CoverageStats {
    /// Per-action coverage statistics keyed by stable action id.
    pub actions: FxHashMap<DetectedActionId, ActionStats>,
    /// Order in which actions were detected (for display)
    pub action_order: Vec<DetectedActionId>,
    /// Total number of distinct states found
    pub total_states: usize,
    /// Total number of transitions explored
    pub total_transitions: usize,
}

impl CoverageStats {
    /// Create new empty coverage stats
    pub fn new() -> Self {
        Self::default()
    }

    /// Register an action (from Next analysis)
    pub fn register_action(&mut self, action: &DetectedAction) {
        if let std::collections::hash_map::Entry::Vacant(entry) = self.actions.entry(action.id) {
            entry.insert(ActionStats::new(action.id, action.name.clone()));
            self.action_order.push(action.id);
        }
    }

    /// Record that an action generated successors
    pub fn record_action(&mut self, action_id: DetectedActionId, successor_count: usize) {
        if let Some(stats) = self.actions.get_mut(&action_id) {
            stats.transitions += successor_count;
            if successor_count > 0 {
                stats.times_fired += 1;
            }
        }
    }

    /// Get list of dead actions (never fired)
    pub fn dead_actions(&self) -> Vec<&str> {
        self.action_order
            .iter()
            .filter_map(|id| self.actions.get(id))
            .filter(|stats| stats.times_fired == 0)
            .map(|stats| stats.name.as_str())
            .collect()
    }

    /// Format coverage as a report string
    pub fn format_report(&self) -> String {
        let mut lines = Vec::new();
        lines.push("=== Coverage Report ===".to_string());
        lines.push(format!(
            "Total: {} distinct states, {} transitions",
            self.total_states, self.total_transitions
        ));
        lines.push(String::new());

        if self.action_order.is_empty() {
            lines.push("No actions detected.".to_string());
        } else {
            lines.push("Actions:".to_string());
            for id in &self.action_order {
                if let Some(stats) = self.actions.get(id) {
                    let status = if stats.times_fired == 0 {
                        " [DEAD]"
                    } else {
                        ""
                    };
                    lines.push(format!(
                        "  {}: {} transitions, fired {} times{}",
                        stats.name, stats.transitions, stats.times_fired, status
                    ));
                }
            }

            let dead = self.dead_actions();
            if !dead.is_empty() {
                lines.push(String::new());
                lines.push(format!(
                    "WARNING: {} dead action(s) (never fired): {}",
                    dead.len(),
                    dead.join(", ")
                ));
            }
        }

        lines.join("\n")
    }
}

/// Statistics for a single action
#[derive(Debug, Clone)]
pub struct ActionStats {
    /// Stable id for the action.
    pub id: DetectedActionId,
    /// Action name (from operator name or generated)
    pub name: String,
    /// Number of transitions generated by this action
    pub transitions: usize,
    /// Number of times this action fired (had at least one successor)
    pub times_fired: usize,
}

impl ActionStats {
    /// Create new stats for an action
    pub fn new(id: DetectedActionId, name: String) -> Self {
        Self {
            id,
            name,
            transitions: 0,
            times_fired: 0,
        }
    }
}

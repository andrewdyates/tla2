/// Print the coverage analysis in human-readable colored table format.
fn print_human_coverage(analysis: &CoverageAnalysis) {
    println!("=== Action Coverage Analysis ===");
    println!();
    println!(
        "Spec: {} (module {})",
        analysis.spec_file, analysis.module
    );
    println!("Status: {}", analysis.status);
    println!(
        "States: {}  |  Transitions: {}  |  Time: {:.2}s",
        format_number(analysis.total_states),
        format_number(analysis.total_transitions),
        analysis.summary.time_seconds,
    );
    if let Some(depth) = analysis.summary.max_depth {
        println!("Max BFS depth: {depth}");
    }
    println!();

    if analysis.actions.is_empty() {
        println!("No action coverage data available.");
        println!();
        println!("To collect coverage data, run:");
        println!("  tla2 check --coverage --output json <spec.tla> > output.json");
        println!("  tla2 coverage output.json");
        return;
    }

    // Print the action table.
    print_action_table(&analysis.actions, analysis.total_transitions);

    // Print summary.
    println!("--- Summary ---");
    println!();
    println!(
        "  Actions exercised: {}",
        analysis.summary.actions_exercised
    );
    if let Some(defined) = analysis.summary.actions_defined {
        let ratio = analysis.summary.coverage_ratio.unwrap_or(0.0);
        println!("  Actions defined:   {defined}");
        println!("  Coverage:          {:.1}%", ratio * 100.0);
    }
    if let Some(hot) = &analysis.summary.hottest_action {
        println!("  Hottest action:    {hot}");
    }
    if let Some(cold) = &analysis.summary.coldest_action {
        println!("  Coldest action:    {cold}");
    }
    println!();

    // Print uncovered actions if any.
    if !analysis.uncovered_actions.is_empty() {
        println!(
            "--- Uncovered Actions ({}) ---",
            analysis.uncovered_actions.len()
        );
        println!();
        for name in &analysis.uncovered_actions {
            println!("  [MISS] {name}");
        }
        println!();
    }
}

/// Print a formatted table of action coverage with ASCII bar chart.
fn print_action_table(actions: &[ActionCoverage], total_transitions: usize) {
    // Compute column widths.
    let max_name_len = actions
        .iter()
        .map(|a| a.name.len())
        .max()
        .unwrap_or(6)
        .max(6);

    let max_count_len = actions
        .iter()
        .map(|a| format_number(a.occurrences).len())
        .max()
        .unwrap_or(5)
        .max(5);

    // Header.
    println!(
        "  {:<name_w$}  {:>count_w$}  {:>6}  {:>5}  {:>5}  Bar",
        "Action",
        "Count",
        "Pct",
        "First",
        "Last",
        name_w = max_name_len,
        count_w = max_count_len,
    );
    println!(
        "  {:-<name_w$}  {:-<count_w$}  {:-<6}  {:-<5}  {:-<5}  {:-<30}",
        "",
        "",
        "",
        "",
        "",
        "",
        name_w = max_name_len,
        count_w = max_count_len,
    );

    // Find the max occurrences for bar scaling.
    let max_occurrences = actions
        .iter()
        .map(|a| a.occurrences)
        .max()
        .unwrap_or(1)
        .max(1);

    for action in actions {
        let percentage = if total_transitions > 0 {
            action.percentage
        } else {
            0.0
        };

        let first = action
            .first_seen_level
            .map(|l| format!("{l}"))
            .unwrap_or_else(|| "-".to_string());

        let last = action
            .last_seen_level
            .map(|l| format!("{l}"))
            .unwrap_or_else(|| "-".to_string());

        // ASCII bar: scale to 30 chars max.
        let bar_len = if max_occurrences > 0 {
            (action.occurrences as f64 / max_occurrences as f64 * 30.0).ceil() as usize
        } else {
            0
        };
        let bar: String = "#".repeat(bar_len.min(30));

        println!(
            "  {:<name_w$}  {:>count_w$}  {:>5.1}%  {:>5}  {:>5}  {bar}",
            action.name,
            format_number(action.occurrences),
            percentage,
            first,
            last,
            name_w = max_name_len,
            count_w = max_count_len,
        );
    }

    println!();
}

/// Format a number with comma separators for readability.
fn format_number(n: usize) -> String {
    let s = n.to_string();
    let mut result = String::with_capacity(s.len() + s.len() / 3);
    for (i, c) in s.chars().rev().enumerate() {
        if i > 0 && i % 3 == 0 {
            result.push(',');
        }
        result.push(c);
    }
    result.chars().rev().collect()
}


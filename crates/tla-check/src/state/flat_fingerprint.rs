/// Strategy enum that dispatches between flat fingerprinting backends.
///
/// Wraps either the XOR-accumulator or xxh3-128 fingerprinter behind a common
/// interface so callers can select the backend at construction time and use a
/// uniform API for both full and incremental fingerprinting.
#[derive(Debug, Clone)]
pub enum FlatFingerprintStrategy {
    /// XOR-accumulator backend: per-slot splitmix64 salts XORed together.
    /// Supports true O(k) incremental diff updates.
    XorAccumulator(FlatFingerprinter),
    /// xxh3-128 backend: single SIMD-accelerated hash call over the byte buffer.
    /// Diff is implemented via buffer-copy + rehash (not truly incremental).
    Xxh3(Xxh3FlatFingerprinter),
}

impl FlatFingerprintStrategy {
    /// Create a strategy using the XOR-accumulator backend.
    #[must_use]
    #[inline]
    pub fn new_xor(num_slots: usize) -> Self {
        Self::XorAccumulator(FlatFingerprinter::new(num_slots))
    }

    /// Create a strategy using the xxh3-128 backend with the default seed.
    #[must_use]
    #[inline]
    pub fn new_xxh3(num_slots: usize) -> Self {
        Self::Xxh3(Xxh3FlatFingerprinter::new(num_slots))
    }

    /// Create a strategy using the xxh3-128 backend with a custom seed.
    #[must_use]
    #[inline]
    pub fn new_xxh3_with_seed(num_slots: usize, seed: u64) -> Self {
        Self::Xxh3(Xxh3FlatFingerprinter::new_with_seed(num_slots, seed))
    }

    /// Compute the full 128-bit fingerprint of a flat state buffer.
    #[must_use]
    #[inline]
    pub fn fingerprint(&self, state: &[i64]) -> u128 {
        match self {
            Self::XorAccumulator(fp) => fp.fingerprint(state),
            Self::Xxh3(fp) => fp.fingerprint(state),
        }
    }

    /// Compute the fingerprint of a successor state given the parent buffer,
    /// the parent fingerprint, and a list of slot changes.
    ///
    /// For the XOR backend this is a true O(k) incremental update.
    /// For the xxh3 backend this copies the parent buffer, applies changes,
    /// and rehashes (the `scratch` buffer is used for the copy).
    #[must_use]
    #[inline]
    pub fn diff(
        &self,
        base: &[i64],
        base_fp: u128,
        changes: &[(usize, i64, i64)],
        scratch: &mut Vec<i64>,
    ) -> u128 {
        match self {
            Self::XorAccumulator(fp) => fp.diff(base, base_fp, changes),
            Self::Xxh3(fp) => fp.diff_with_buffer(base, changes, scratch),
        }
    }

    /// Number of slots this strategy was configured for.
    #[must_use]
    #[inline]
    pub fn num_slots(&self) -> usize {
        match self {
            Self::XorAccumulator(fp) => fp.num_slots(),
            Self::Xxh3(fp) => fp.num_slots(),
        }
    }

    /// Returns `true` if this strategy uses the xxh3-128 backend.
    #[must_use]
    #[inline]
    pub fn is_xxh3(&self) -> bool {
        matches!(self, Self::Xxh3(_))
    }
}



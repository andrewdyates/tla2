/// Reinterpret an `i64` slice as a byte slice for hashing.
///
/// Note: the output depends on platform endianness. This is acceptable
/// for model checking — all fingerprinting happens on the same machine
/// within a single run. Cross-platform reproducibility is not required.
#[inline(always)]
fn i64_slice_as_bytes(state: &[i64]) -> &[u8] {
    if state.is_empty() {
        return &[];
    }
    // SAFETY: i64 is a Copy type with alignment >= 1 (the alignment of u8).
    // The pointer cast from *const i64 to *const u8 is valid because u8
    // has alignment 1 and accepts any pointer. The byte length is exactly
    // state.len() * 8, which cannot overflow for any slice that already
    // exists in memory (the original allocation was at least that large).
    // The resulting lifetime is tied to the input slice via the function
    // signature.
    unsafe { std::slice::from_raw_parts(state.as_ptr().cast::<u8>(), state.len() * 8) }
}

/// Compute a 128-bit fingerprint of a flat `i64` state array using
/// xxHash3-128.
///
/// This hashes the raw byte representation of the state buffer in a
/// single SIMD-accelerated call. For a typical 15-slot (120-byte) state
/// this completes in ~2-5ns — roughly 25x faster than tree-walking FP64.
///
/// The fingerprint is deterministic for a given byte representation.
/// Because it operates on the native byte layout of `i64`, the output
/// depends on platform endianness (always little-endian on x86_64/aarch64).
///
/// Part of #3987.
#[must_use]
#[inline]
pub fn fingerprint_flat_xxh3(state: &[i64]) -> u128 {
    xxhash_rust::xxh3::xxh3_128(i64_slice_as_bytes(state))
}

/// Compute a 128-bit fingerprint of a flat `i64` state array using
/// xxHash3-128 with a custom seed.
///
/// The seed provides domain separation: different seeds produce
/// independent fingerprint spaces. Useful when multiple fingerprint
/// tables coexist (e.g., separate dedup for BFS levels).
///
/// Part of #3987.
#[must_use]
#[inline]
pub fn fingerprint_flat_xxh3_with_seed(state: &[i64], seed: u64) -> u128 {
    xxhash_rust::xxh3::xxh3_128_with_seed(i64_slice_as_bytes(state), seed)
}

/// xxHash3-128 based flat state fingerprinter.
///
/// Uses SIMD-accelerated xxh3_128 for full-state fingerprinting. For
/// incremental diff fingerprinting, applies changes to a scratch buffer
/// and re-hashes the full buffer (still O(n) but extremely fast for the
/// small n typical of TLA+ states: 5-20 slots = 40-160 bytes).
///
/// Compared to [`FlatFingerprinter`] (XOR-accumulator):
/// - Full fingerprint: xxh3 is faster (single SIMD call vs per-slot mix)
/// - Diff fingerprint: XOR-accumulator is O(k) vs xxh3's O(n), but for
///   n < 20 slots the difference is negligible and xxh3's raw throughput
///   dominates
/// - No salts needed for xxh3 (the hash itself provides mixing)
///
/// # Example
///
/// ```rust,no_run
/// use tla_check::state::flat_fingerprint::Xxh3FlatFingerprinter;
///
/// let fp = Xxh3FlatFingerprinter::new(5);
/// let state = vec![1i64, 2, 3, 4, 5];
/// let hash = fp.fingerprint(&state);
///
/// // Diff: apply changes via scratch buffer
/// let mut scratch = Vec::new();
/// let new_hash = fp.diff_with_buffer(&state, &[(2, 3, 99)], &mut scratch);
/// ```
///
/// Part of #3987.
#[derive(Debug, Clone)]
pub struct Xxh3FlatFingerprinter {
    num_slots: usize,
    seed: u64,
}

impl Xxh3FlatFingerprinter {
    /// Create a new xxh3 fingerprinter for states with `num_slots` variables.
    ///
    /// Uses seed 0 (default xxh3 behavior).
    #[must_use]
    pub fn new(num_slots: usize) -> Self {
        Self {
            num_slots,
            seed: 0,
        }
    }

    /// Create a new xxh3 fingerprinter with a custom seed.
    ///
    /// Different seeds produce independent fingerprint spaces.
    #[must_use]
    pub fn new_with_seed(num_slots: usize, seed: u64) -> Self {
        Self { num_slots, seed }
    }

    /// Full 128-bit xxh3 fingerprint of a flat state array.
    ///
    /// Single SIMD-accelerated hash call on the byte representation.
    #[must_use]
    #[inline]
    pub fn fingerprint(&self, state: &[i64]) -> u128 {
        debug_assert_eq!(
            state.len(),
            self.num_slots,
            "Xxh3FlatFingerprinter::fingerprint: state len {} != num_slots {}",
            state.len(),
            self.num_slots
        );
        fingerprint_flat_xxh3_with_seed(state, self.seed)
    }

    /// Incremental fingerprint by applying changes to a scratch buffer.
    ///
    /// Copies `base` into `scratch`, applies each `(slot, _old, new)` change,
    /// then xxh3-hashes the result. The `scratch` buffer is reused across
    /// calls to avoid repeated allocation.
    ///
    /// This is O(n) where n = num_slots, but for typical TLA+ states
    /// (5-20 slots, 40-160 bytes) the memcpy + xxh3 SIMD hash is faster
    /// than any incremental scheme.
    ///
    /// # Arguments
    ///
    /// * `base` - The base state buffer (pre-change).
    /// * `changes` - Slice of `(slot_index, old_value, new_value)` triples.
    ///   The `old_value` field is unused but kept for API compatibility with
    ///   [`FlatFingerprinter::diff`].
    /// * `scratch` - Reusable buffer; resized and overwritten each call.
    #[must_use]
    #[inline]
    pub fn diff_with_buffer(
        &self,
        base: &[i64],
        changes: &[(usize, i64, i64)],
        scratch: &mut Vec<i64>,
    ) -> u128 {
        debug_assert_eq!(
            base.len(),
            self.num_slots,
            "Xxh3FlatFingerprinter::diff_with_buffer: base len {} != num_slots {}",
            base.len(),
            self.num_slots
        );
        scratch.clear();
        scratch.extend_from_slice(base);
        for &(slot, _old, new) in changes {
            debug_assert!(
                slot < scratch.len(),
                "Xxh3FlatFingerprinter::diff_with_buffer: slot {} out of bounds (len {})",
                slot,
                scratch.len()
            );
            scratch[slot] = new;
        }
        fingerprint_flat_xxh3_with_seed(scratch, self.seed)
    }

    /// Number of slots this fingerprinter was configured for.
    #[must_use]
    #[inline]
    pub fn num_slots(&self) -> usize {
        self.num_slots
    }

    /// The seed used for xxh3 hashing.
    #[must_use]
    #[inline]
    pub fn seed(&self) -> u64 {
        self.seed
    }
}



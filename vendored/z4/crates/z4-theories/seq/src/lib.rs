#![forbid(unsafe_code)]
//! Z4 Seq - Generic sequence theory solver
//!
//! Implements the theory of parametric sequences `(Seq T)` for arbitrary
//! element types. This is distinct from the string theory: `Sort::String`
//! continues to use the specialized string solver, while `Sort::Seq(T)` uses
//! this solver.
//!
//! ## Axioms handled by this crate
//!
//! - **Unit injectivity**: `seq.unit(a) = seq.unit(b) → a = b`
//! - **Unit-empty conflict**: `seq.unit(x) ≠ seq.empty`
//!
//! Length and nth axioms (length of unit/empty/concat, length non-negative,
//! nth of unit) are generated by the DPLL(T) adapter in
//! `z4-dpll/src/executor/theories/seq.rs`.
#![warn(missing_docs)]
#![warn(clippy::all)]

mod theory_impl;
#[cfg(kani)]
mod verification;

#[cfg(not(kani))]
use hashbrown::HashMap;
use num_bigint::BigInt;
#[cfg(kani)]
use z4_core::kani_compat::DetHashMap as HashMap;
use z4_core::term::{Constant, TermData, TermId, TermStore};
use z4_core::{
    DiscoveredEquality, EqualityPropagationResult, TheoryLit, TheoryPropagation, TheoryResult,
    TheorySolver,
};

/// Model for the sequence theory — maps Seq-sorted variables to concrete
/// sequences of element values (formatted strings).
#[derive(Debug, Clone, Default)]
pub struct SeqModel {
    /// Maps Seq-sorted variable term IDs to their concrete sequence of
    /// element values (as formatted strings).
    pub values: HashMap<TermId, Vec<String>>,
}

/// Sequence theory solver.
///
/// Handles unit injectivity and unit-empty conflict detection.
/// Eagerly propagates equalities from `seq.unit` congruence.
pub struct SeqSolver<'a> {
    terms: &'a TermStore,
    /// Current Boolean assignments: atom → value.
    assigns: HashMap<TermId, bool>,
    /// Trail for backtracking: (atom, previous_value).
    trail: Vec<(TermId, Option<bool>)>,
    /// Scope markers (trail positions for push/pop).
    scopes: Vec<usize>,
    /// Cached seq.unit terms: unit_term → element_arg.
    unit_cache: HashMap<TermId, TermId>,
    /// Cached seq.len terms: len_term → sequence_arg.
    len_cache: HashMap<TermId, TermId>,
    /// Cached seq.++ terms: concat_term → [arg1, arg2, ...].
    concat_cache: HashMap<TermId, Vec<TermId>>,
    /// Cached seq.empty terms (zero-arity apps).
    empty_cache: Vec<TermId>,
    /// Cached seq.nth terms: nth_term → (seq_arg, index_arg).
    nth_cache: HashMap<TermId, (TermId, TermId)>,
    /// Equality atoms: eq_term → (lhs, rhs).
    equality_cache: HashMap<TermId, (TermId, TermId)>,
    /// Pending propagations.
    pending_propagations: Vec<TheoryPropagation>,
    /// Pending equalities for Nelson-Oppen.
    pending_equalities: Vec<DiscoveredEquality>,
    /// Shared equalities received from Nelson-Oppen exchange (EUF → Seq).
    /// Each entry is (lhs, rhs, reason). Checked by `check()` for
    /// unit-empty conflicts and unit injectivity. See #5951.
    shared_equalities: Vec<(TermId, TermId, Vec<TheoryLit>)>,
    /// Trail of shared equality indices for backtracking.
    shared_eq_scopes: Vec<usize>,
    /// Dirty flag: need to re-check.
    dirty: bool,
}

impl<'a> SeqSolver<'a> {
    /// Create a new sequence solver with a reference to the term store.
    pub fn new(terms: &'a TermStore) -> Self {
        Self {
            terms,
            assigns: HashMap::new(),
            trail: Vec::new(),
            scopes: Vec::new(),
            unit_cache: HashMap::new(),
            len_cache: HashMap::new(),
            concat_cache: HashMap::new(),
            empty_cache: Vec::new(),
            nth_cache: HashMap::new(),
            equality_cache: HashMap::new(),
            pending_propagations: Vec::new(),
            pending_equalities: Vec::new(),
            shared_equalities: Vec::new(),
            shared_eq_scopes: Vec::new(),
            dirty: false,
        }
    }

    /// Returns all cached unit term IDs.
    pub fn unit_terms(&self) -> impl Iterator<Item = TermId> + '_ {
        self.unit_cache.keys().copied()
    }

    /// Returns the element argument for a unit term, if cached.
    pub fn unit_element(&self, term: TermId) -> Option<TermId> {
        self.unit_cache.get(&term).copied()
    }

    /// Returns all cached empty term IDs.
    pub fn empty_terms(&self) -> impl Iterator<Item = TermId> + '_ {
        self.empty_cache.iter().copied()
    }

    /// Extract a model for Seq-sorted variables.
    ///
    /// Builds concrete sequence values from asserted equalities and the
    /// structure of unit/concat/empty terms.
    pub fn extract_model(&self) -> SeqModel {
        let mut values = HashMap::new();

        // For each Seq-sorted variable, try to determine its concrete value
        // from asserted equalities.
        for (&eq_term, &(lhs, rhs)) in &self.equality_cache {
            if self.assigns.get(&eq_term) != Some(&true) {
                continue;
            }
            Self::try_insert_seq_value(&mut values, lhs, rhs, self.terms, self);
            Self::try_insert_seq_value(&mut values, rhs, lhs, self.terms, self);
        }

        // Also check shared equalities from Nelson-Oppen exchange (EUF → Seq).
        // These are equalities discovered by EUF congruence/transitivity that
        // involve Seq-sorted terms but were never direct assertion atoms.
        for (lhs, rhs, _reason) in &self.shared_equalities {
            Self::try_insert_seq_value(&mut values, *lhs, *rhs, self.terms, self);
            Self::try_insert_seq_value(&mut values, *rhs, *lhs, self.terms, self);
        }

        SeqModel { values }
    }

    /// If `candidate` is a Seq-sorted variable and `value_term` can be
    /// concretized, insert the mapping into `values`.
    fn try_insert_seq_value(
        values: &mut HashMap<TermId, Vec<String>>,
        candidate: TermId,
        value_term: TermId,
        terms: &TermStore,
        solver: &Self,
    ) {
        if let TermData::Var(_, _) = terms.get(candidate) {
            if terms.sort(candidate).is_seq() {
                if let Some(elems) = solver.extract_seq_value(value_term) {
                    values.insert(candidate, elems);
                }
            }
        }
    }

    /// Try to extract a concrete sequence value from a term.
    /// Returns `Some(vec_of_element_strings)` for constructible terms,
    /// `None` for symbolic terms that can't be concretized.
    fn extract_seq_value(&self, term: TermId) -> Option<Vec<String>> {
        // seq.empty → []
        if self.empty_cache.contains(&term) {
            return Some(Vec::new());
        }
        // seq.unit(x) → [format(x)]
        if let Some(&elem) = self.unit_cache.get(&term) {
            return Some(vec![self.format_term_value(elem)]);
        }
        // seq.++(a, b) → extract(a) ++ extract(b)
        if let Some(args) = self.concat_cache.get(&term) {
            let mut result = Vec::new();
            for &arg in args {
                let sub = self.extract_seq_value(arg)?;
                result.extend(sub);
            }
            return Some(result);
        }
        None
    }

    /// Format a term's value for SMT-LIB model output.
    fn format_term_value(&self, term: TermId) -> String {
        match self.terms.get(term) {
            TermData::Const(c) => match c {
                Constant::Bool(b) => format!("{b}"),
                Constant::Int(n) => {
                    if *n < BigInt::from(0) {
                        format!("(- {})", -n)
                    } else {
                        format!("{n}")
                    }
                }
                Constant::Rational(r) => {
                    let n = r.0.numer();
                    let d = r.0.denom();
                    if *d == BigInt::from(1) {
                        format!("{n}")
                    } else {
                        format!("(/ {n} {d})")
                    }
                }
                Constant::BitVec { value, width } => {
                    format!("(_ bv{value} {width})")
                }
                Constant::String(s) => format!("\"{s}\""),
                _ => format!("{c:?}"),
            },
            TermData::Var(name, _) => name.clone(),
            _ => format!("t{}", term.0),
        }
    }

    /// Register a term and its subterms in the solver's caches.
    fn cache_term(&mut self, term: TermId) {
        if let TermData::App(sym, args) = self.terms.get(term) {
            let name = sym.name();
            match name {
                "seq.unit" if args.len() == 1 => {
                    self.unit_cache.insert(term, args[0]);
                }
                "seq.len" if args.len() == 1 => {
                    self.len_cache.insert(term, args[0]);
                }
                "seq.++" if args.len() >= 2 => {
                    let sub_args = args.clone();
                    self.concat_cache.insert(term, sub_args.clone());
                    // Recursively cache concat arguments so extract_seq_value
                    // can resolve nested seq.unit / seq.empty / seq.++ terms.
                    for arg in sub_args {
                        self.cache_term(arg);
                    }
                }
                "seq.empty" if args.is_empty() => {
                    if !self.empty_cache.contains(&term) {
                        self.empty_cache.push(term);
                    }
                }
                "seq.nth" if args.len() == 2 => {
                    self.nth_cache.insert(term, (args[0], args[1]));
                }
                "=" if args.len() == 2 => {
                    // Cache equality atoms involving Seq sorts.
                    // Recursively cache both sides so extract_model can
                    // concretize the Seq value from the equality.
                    let lhs_sort = self.terms.sort(args[0]);
                    let rhs_sort = self.terms.sort(args[1]);
                    if lhs_sort.is_seq() || rhs_sort.is_seq() {
                        let lhs = args[0];
                        let rhs = args[1];
                        self.equality_cache.insert(term, (lhs, rhs));
                        self.cache_term(lhs);
                        self.cache_term(rhs);
                    }
                }
                _ => {}
            }
        }
    }

    /// Check for unit injectivity: seq.unit(a) = seq.unit(b) → a = b.
    /// Produces Nelson-Oppen equalities.
    fn check_unit_injectivity(&mut self) {
        // From directly asserted equality atoms.
        for (&eq_term, &(lhs, rhs)) in &self.equality_cache {
            if self.assigns.get(&eq_term) != Some(&true) {
                continue;
            }
            if let (Some(&elem_a), Some(&elem_b)) =
                (self.unit_cache.get(&lhs), self.unit_cache.get(&rhs))
            {
                if elem_a != elem_b {
                    self.pending_equalities.push(DiscoveredEquality {
                        lhs: elem_a,
                        rhs: elem_b,
                        reason: vec![TheoryLit {
                            term: eq_term,
                            value: true,
                        }],
                    });
                }
            }
        }

        // From shared equalities received via Nelson-Oppen (#5951).
        let shared = std::mem::take(&mut self.shared_equalities);
        for &(lhs, rhs, ref reason) in &shared {
            if let (Some(&elem_a), Some(&elem_b)) =
                (self.unit_cache.get(&lhs), self.unit_cache.get(&rhs))
            {
                if elem_a != elem_b {
                    self.pending_equalities.push(DiscoveredEquality {
                        lhs: elem_a,
                        rhs: elem_b,
                        reason: reason.clone(),
                    });
                }
            }
        }
        self.shared_equalities = shared;
    }

    /// Check for basic conflict: seq.unit(a) = seq.empty is unsat.
    fn check_unit_empty_conflict(&self) -> Option<Vec<TheoryLit>> {
        // Check directly asserted equality atoms.
        for (&eq_term, &(lhs, rhs)) in &self.equality_cache {
            if self.assigns.get(&eq_term) != Some(&true) {
                continue;
            }
            let lhs_is_unit = self.unit_cache.contains_key(&lhs);
            let rhs_is_unit = self.unit_cache.contains_key(&rhs);
            let lhs_is_empty = self.empty_cache.contains(&lhs);
            let rhs_is_empty = self.empty_cache.contains(&rhs);

            if (lhs_is_unit && rhs_is_empty) || (rhs_is_unit && lhs_is_empty) {
                return Some(vec![TheoryLit {
                    term: eq_term,
                    value: true,
                }]);
            }
        }

        // Check shared equalities received via Nelson-Oppen (#5951).
        for (lhs, rhs, reason) in &self.shared_equalities {
            let lhs_is_unit = self.unit_cache.contains_key(lhs);
            let rhs_is_unit = self.unit_cache.contains_key(rhs);
            let lhs_is_empty = self.empty_cache.contains(lhs);
            let rhs_is_empty = self.empty_cache.contains(rhs);

            if (lhs_is_unit && rhs_is_empty) || (rhs_is_unit && lhs_is_empty) {
                return Some(reason.clone());
            }
        }

        None
    }
}


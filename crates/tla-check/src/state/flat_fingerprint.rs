    fn test_cross_check_dedup_10k_states() {
        // Acceptance criterion: "first 10K states match old fingerprint dedup
        // behavior." Both XOR and xxh3 backends must produce distinct
        // fingerprints for distinct states and identical fingerprints for
        // identical states.
        let num_slots = 5;
        let xor = FlatFingerprintStrategy::new_xor(num_slots);
        let xxh3 = FlatFingerprintStrategy::new_xxh3(num_slots);

        let mut xor_fps = std::collections::HashSet::new();
        let mut xxh3_fps = std::collections::HashSet::new();

        for i in 0i64..10_000 {
            // Generate a distinct 5-slot state for each i using a simple
            // deterministic scheme that exercises all slots.
            let state = vec![
                i,
                i.wrapping_mul(7).wrapping_add(13),
                i.wrapping_mul(31).wrapping_sub(97),
                i.wrapping_mul(127).wrapping_add(3),
                i.wrapping_mul(1009),
            ];

            let xor_fp = xor.fingerprint(&state);
            let xxh3_fp = xxh3.fingerprint(&state);

            assert!(
                xor_fps.insert(xor_fp),
                "XOR collision at i={}: fingerprint {:032x} already seen",
                i,
                xor_fp,
            );
            assert!(
                xxh3_fps.insert(xxh3_fp),
                "xxh3 collision at i={}: fingerprint {:032x} already seen",
                i,
                xxh3_fp,
            );

            // Verify determinism: same state produces same fingerprint.
            assert_eq!(
                xor.fingerprint(&state),
                xor_fp,
                "XOR fingerprint not deterministic at i={}",
                i,
            );
            assert_eq!(
                xxh3.fingerprint(&state),
                xxh3_fp,
                "xxh3 fingerprint not deterministic at i={}",
                i,
            );
        }

        assert_eq!(xor_fps.len(), 10_000, "XOR: expected 10K distinct fingerprints");
        assert_eq!(xxh3_fps.len(), 10_000, "xxh3: expected 10K distinct fingerprints");
    }


//! `tla2 coverage` -- model checking coverage analysis.
//!
//! Reads a JSON output file produced by `tla2 check --output json` and reports
//! which actions (Next disjuncts) were exercised during model checking, how many
//! states each generated, and what percentage of actions were explored.
//!
//! Two data sources are used:
//! 1. `actions_detected` — per-action occurrence counts from the JSON output
//! 2. Counterexample trace states — action names from the trace (when present)
//!
//! When a TLA+ spec file is provided (`--spec`), the command cross-references
//! detected actions against the actual Next disjuncts defined in the spec to
//! identify unexplored actions.

use std::collections::HashMap;
use std::path::Path;

use anyhow::{Context, Result};
use serde::Serialize;
use tla_check::{ActionInfo, JsonOutput};

use crate::cli_schema::CoverageOutputFormat;

// ---------------------------------------------------------------------------
// Public entry point
// ---------------------------------------------------------------------------

/// Run the coverage analysis command.
pub(crate) fn cmd_coverage(
    trace_file: &Path,
    spec: Option<&Path>,
    config: Option<&Path>,
    format: CoverageOutputFormat,
) -> Result<()> {
    // 1. Read and parse the JSON output file.
    let json_content = std::fs::read_to_string(trace_file)
        .with_context(|| format!("Failed to read trace file: {}", trace_file.display()))?;

    let output: JsonOutput = serde_json::from_str(&json_content)
        .with_context(|| format!("Failed to parse JSON from: {}", trace_file.display()))?;

    // 2. Build the coverage analysis.
    let analysis = build_coverage_analysis(&output, spec, config)?;

    // 3. Emit output.
    match format {
        CoverageOutputFormat::Human => print_human_coverage(&analysis),
        CoverageOutputFormat::Json => {
            let json = serde_json::to_string_pretty(&analysis)
                .context("Failed to serialize coverage analysis to JSON")?;
            println!("{json}");
        }
    }

    Ok(())
}

// ---------------------------------------------------------------------------
// Analysis types
// ---------------------------------------------------------------------------

/// Complete coverage analysis result.
#[derive(Debug, Serialize)]
struct CoverageAnalysis {
    /// Source file that was checked.
    spec_file: String,
    /// Module name.
    module: String,
    /// Overall status of the check.
    status: String,
    /// Total states found during model checking.
    total_states: usize,
    /// Total transitions.
    total_transitions: usize,
    /// Per-action coverage metrics.
    actions: Vec<ActionCoverage>,
    /// Actions defined in the spec but never exercised (requires --spec).
    #[serde(skip_serializing_if = "Vec::is_empty")]
    uncovered_actions: Vec<String>,
    /// Summary metrics.
    summary: CoverageSummary,
}

/// Coverage metrics for a single action.
#[derive(Debug, Serialize)]
struct ActionCoverage {
    /// Action name (e.g., "Increment", "Decrement").
    name: String,
    /// Action ID (e.g., "detected:0").
    #[serde(skip_serializing_if = "String::is_empty")]
    id: String,
    /// Number of state transitions generated by this action.
    occurrences: usize,
    /// Percentage of all transitions attributed to this action.
    percentage: f64,
    /// First BFS level where this action was observed (from trace, if available).
    #[serde(skip_serializing_if = "Option::is_none")]
    first_seen_level: Option<usize>,
    /// Last BFS level where this action was observed (from trace, if available).
    #[serde(skip_serializing_if = "Option::is_none")]
    last_seen_level: Option<usize>,
}

/// Summary of overall action coverage.
#[derive(Debug, Serialize)]
struct CoverageSummary {
    /// Total number of distinct actions observed.
    actions_exercised: usize,
    /// Total number of actions defined in the spec (requires --spec).
    #[serde(skip_serializing_if = "Option::is_none")]
    actions_defined: Option<usize>,
    /// Fraction of defined actions that were exercised (requires --spec).
    #[serde(skip_serializing_if = "Option::is_none")]
    coverage_ratio: Option<f64>,
    /// The most frequently exercised action.
    #[serde(skip_serializing_if = "Option::is_none")]
    hottest_action: Option<String>,
    /// The least frequently exercised action (among those exercised).
    #[serde(skip_serializing_if = "Option::is_none")]
    coldest_action: Option<String>,
    /// Wall-clock time of the model checking run.
    time_seconds: f64,
    /// Maximum BFS depth reached.
    #[serde(skip_serializing_if = "Option::is_none")]
    max_depth: Option<usize>,
}


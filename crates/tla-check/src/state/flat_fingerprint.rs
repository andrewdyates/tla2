/// Per-slot Zobrist table for O(k) incremental 64-bit fingerprinting.
///
/// Fingerprint = XOR of `zobrist[i] * mix(state[i])` for all i.
///
/// This enables O(changed) incremental updates: when slot `i` changes from
/// `old` to `new`, the new fingerprint is:
///
/// ```text
/// new_fp = old_fp ^ zobrist_contribution(i, old) ^ zobrist_contribution(i, new)
/// ```
///
/// The 64-bit output is compatible with `Fingerprint(u64)` for direct use in
/// the model checker's dedup table.
///
/// # Compared to `FlatFingerprinter` (128-bit XOR accumulator)
///
/// `ZobristFlatFingerprinter` produces `u64` fingerprints compatible with the
/// existing `Fingerprint(u64)` type, while `FlatFingerprinter` produces `u128`.
/// The Zobrist approach uses the same mathematical structure (XOR of per-slot
/// contributions) but targets the 64-bit fingerprint space used by the BFS
/// dedup table.
///
/// # Thread safety
///
/// The Zobrist table is immutable after construction and can be shared across
/// threads via `Arc<ZobristFlatFingerprinter>`.
///
/// Part of #3987 Phase 4.
#[derive(Debug, Clone)]
pub struct ZobristFlatFingerprinter {
    /// Per-slot random values. Length == num state variable slots.
    table: Vec<u64>,
}

impl ZobristFlatFingerprinter {
    /// Create a new Zobrist fingerprinter for states with `num_slots` variables.
    ///
    /// The table is initialized deterministically from a fixed seed via
    /// splitmix64, ensuring reproducible fingerprints across runs.
    #[must_use]
    pub fn new(num_slots: usize) -> Self {
        Self {
            table: generate_zobrist_table(num_slots),
        }
    }

    /// Create a fingerprinter from a pre-computed table.
    #[must_use]
    pub fn from_table(table: Vec<u64>) -> Self {
        Self { table }
    }

    /// Compute the full 64-bit Zobrist fingerprint of a flat state array.
    ///
    /// `fingerprint = XOR_i(zobrist_slot_contribution(table[i], state[i]))`.
    #[must_use]
    #[inline]
    pub fn fingerprint(&self, state: &[i64]) -> u64 {
        zobrist_fingerprint(state, &self.table)
    }

    /// Compute the incremental fingerprint after slot changes.
    ///
    /// O(k) where k = number of changed slots.
    ///
    /// # Arguments
    ///
    /// * `base_fp` - The fingerprint of the base (pre-change) state.
    /// * `changes` - Slice of `(slot_index, old_value, new_value)` triples.
    #[must_use]
    #[inline]
    pub fn incremental(
        &self,
        base_fp: u64,
        changes: &[(usize, i64, i64)],
    ) -> u64 {
        zobrist_incremental(base_fp, &self.table, changes)
    }

    /// Number of slots this fingerprinter was configured for.
    #[must_use]
    #[inline]
    pub fn num_slots(&self) -> usize {
        self.table.len()
    }

    /// Borrow the Zobrist table.
    #[must_use]
    #[inline]
    pub fn table(&self) -> &[u64] {
        &self.table
    }
}

/// Seed for Zobrist table generation.
/// Different from `FLAT_FP_SALT_SEED` to ensure independent table values.
const ZOBRIST_SEED: u64 = 0xA3B195354A39B70D;

/// Generate a deterministic Zobrist table via splitmix64.
#[must_use]
pub fn generate_zobrist_table(num_slots: usize) -> Vec<u64> {
    let mut table = Vec::with_capacity(num_slots);
    let mut state = ZOBRIST_SEED;
    for _ in 0..num_slots {
        state = splitmix64_mix(state);
        table.push(state);
    }
    table
}

/// Compute a single slot's Zobrist contribution (64-bit).
///
/// Mixes the slot's value with its Zobrist table entry to produce a
/// well-distributed 64-bit contribution. Uses splitmix64 mixing of
/// `table_entry ^ value_bits` for avalanche properties.
#[inline(always)]
fn zobrist_slot_contribution(table_entry: u64, value: i64) -> u64 {
    let bits = value as u64;
    splitmix64_mix(table_entry ^ bits)
}

/// Compute the full 64-bit Zobrist fingerprint.
#[must_use]
#[inline]
fn zobrist_fingerprint(state: &[i64], table: &[u64]) -> u64 {
    debug_assert_eq!(
        state.len(),
        table.len(),
        "zobrist_fingerprint: state len {} != table len {}",
        state.len(),
        table.len()
    );
    let mut fp = 0u64;
    for (i, &val) in state.iter().enumerate() {
        fp ^= zobrist_slot_contribution(table[i], val);
    }
    fp
}

/// Compute incremental 64-bit Zobrist fingerprint from a base fingerprint.
///
/// XOR-based: XOR out old contributions, XOR in new ones.
#[must_use]
#[inline]
fn zobrist_incremental(
    base_fp: u64,
    table: &[u64],
    changes: &[(usize, i64, i64)],
) -> u64 {
    let mut delta = 0u64;
    for &(slot, old_val, new_val) in changes {
        debug_assert!(
            slot < table.len(),
            "zobrist_incremental: slot {} out of bounds (table len {})",
            slot,
            table.len()
        );
        let old_contrib = zobrist_slot_contribution(table[slot], old_val);
        let new_contrib = zobrist_slot_contribution(table[slot], new_val);
        delta ^= old_contrib ^ new_contrib;
    }
    base_fp ^ delta
}

